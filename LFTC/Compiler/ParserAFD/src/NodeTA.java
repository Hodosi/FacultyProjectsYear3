public class NodeTA {
    private final String atomLexcial;
    private final int codAtom;

    public NodeTA(String atomLexcial, int codAtom) {
        this.atomLexcial = atomLexcial;
        this.codAtom = codAtom;
    }

    public String getAtomLexcial() {
        return atomLexcial;
    }

    public int getCodAtom() {
        return codAtom;
    }
}
